// Sum of Array Elements:
//     Write a function that calculates the sum of all elements in an array using a loop.
const sumOfAllElements = (arr) => {
    let sum = 0;
    for(let i = 0; i < arr.length; i++) {
        return sum += arr[i];
    }
    return sum;
}

